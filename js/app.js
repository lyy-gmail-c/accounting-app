// ============ 本地日期工具 ============
// 使用本地时间（系统时区），避免 toISOString() 的 UTC 偏差
function localToday(d) {
  d = d || new Date();
  const y = d.getFullYear();
  const m = String(d.getMonth() + 1).padStart(2, '0');
  const day = String(d.getDate()).padStart(2, '0');
  return `${y}-${m}-${day}`;
}

function localMonth(d) {
  d = d || new Date();
  const y = d.getFullYear();
  const m = String(d.getMonth() + 1).padStart(2, '0');
  return `${y}-${m}`;
}

/** 把任意 Date 格式化为本地 YYYY-MM-DD（用于 new Date(year, month-offset, 1) 等场景） */
function localDateStr(d) {
  return localToday(d);
}

// ============ 应用核心 ============
const App = {
  isMobile: window.innerWidth < 768,

  async init() {
    await initDB();
    this.isMobile = window.innerWidth < 768;

    if (this.isMobile) {
      document.querySelector('.pc-layout')?.classList.add('hidden');
      document.querySelector('.mobile-layout')?.classList.remove('hidden');
      MobileUI.init();
    } else {
      document.querySelector('.mobile-layout')?.classList.add('hidden');
      document.querySelector('.pc-layout')?.classList.remove('hidden');
      DesktopUI.init();
    }

    // 监听窗口变化
    window.addEventListener('resize', () => {
      const nowMobile = window.innerWidth < 768;
      if (nowMobile !== this.isMobile) {
        this.isMobile = nowMobile;
        location.reload(); // 简单处理：刷新切换端
      }
    });
  },

  showToast(msg) {
    let toast = document.querySelector('.toast');
    if (!toast) {
      toast = document.createElement('div');
      toast.className = 'toast';
      document.body.appendChild(toast);
    }
    // 清除上一次的定时器
    clearTimeout(App._toastTimer);
    // 先强制移除动画状态
    toast.classList.remove('show');
    toast.textContent = msg;
    // 双帧 rAF 确保浏览器完成样式计算后再触发动画
    requestAnimationFrame(() => {
      requestAnimationFrame(() => {
        toast.classList.add('show');
        App._toastTimer = setTimeout(() => toast.classList.remove('show'), 2000);
      });
    });
  },
  _toastTimer: null,

  /** HTML 转义，防止备注等用户输入被当成标签解析 */
  escapeHtml(s) {
    if (s == null) return '';
    return String(s)
      .replace(/&/g, '&amp;')
      .replace(/</g, '&lt;')
      .replace(/>/g, '&gt;')
      .replace(/"/g, '&quot;')
      .replace(/'/g, '&#39;');
  },
};

